interface BottomNavProps {
  activeTab?: string;
  onTabChange?: (tabId: string) => void;
}

export default function BottomNav({ activeTab = 'menu', onTabChange }: BottomNavProps) {
  const handleTabClick = (tabId: string) => {
    if (tabId === 'canal') {
      // Ouvrir Telegram
      window.open('https://t.me/hashburgerchannel', '_blank');
    } else if (tabId === 'admin') {
      // Ouvrir panel admin
      window.open('/admin', '_blank');
    } else if (onTabChange) {
      onTabChange(tabId);
    }
  };

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-black border-t border-white/20 z-40">
      <div className="flex items-center justify-around py-2">
        {navItems.map((item) => (
          <button
            key={item.id}
            onClick={() => handleTabClick(item.id)}
            className={`flex flex-col items-center justify-center py-2 px-3 rounded-lg transition-all duration-200 ${
              activeTab === item.id
                ? 'text-white bg-gray-800 border border-white/20'
                : 'text-gray-400 hover:text-white hover:bg-gray-800'
            }`}
          >
            <div className={`transition-transform duration-200 ${
              activeTab === item.id ? 'scale-110' : ''
            }`}>
              {item.icon}
            </div>
            <span className="text-xs font-medium mt-1 tracking-wide">
              {item.label}
            </span>
          </button>
        ))}
      </div>
    </nav>
  );
}